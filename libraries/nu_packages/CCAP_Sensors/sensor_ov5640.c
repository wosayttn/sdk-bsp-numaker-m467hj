static ccap_sensor_dev_t nu_create_ov5640(ccap_sensor_io *psIo, const char *szName)
{
    rt_err_t ret;
    ccap_sensor_dev_t pdev = rt_malloc(sizeof(ccap_sensor_dev));

    RT_ASSERT(pdev);
    RT_ASSERT(psIo);
    RT_ASSERT(szName);

    pdev->psIo  = psIo;
    ret = ccap_sensor_register(&pdev->device, szName, (void *)&ov5640_priv);

    if (ret != RT_EOK)
    {
        rt_free(pdev);
        pdev = RT_NULL;
    }

    return pdev;
}

